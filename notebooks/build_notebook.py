    "# Cell 4: PositionResolver\n",
    "# 替代: YOLOService.identifyObj() + HandEyeService.transform()\n",
    "# ============================================================\n",
    "\n",
    "class PositionResolver:\n",
    "    \"\"\"v2 固定位置查表器\n",
    "\n",
    "    替代 v1 的 YOLO + 手眼标定两步:\n",
    "    - v1: 像素坐标 -> HandEyeService.transform() -> 机械臂坐标\n",
    "    - v2: 器械ID -> ConfigLoader.get_instrument() -> 机械臂坐标(直接)\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, config_loader: ConfigLoader):\n",
    "        self._config = config_loader\n",
    "\n",
    "    def resolve(self, instrument_id: int) -> dict:\n",
    "        \"\"\"查表获取器械的机械臂坐标和夹爪参数\"\"\"\n",
    "        inst = self._config.get_instrument(instrument_id)\n",
    "        return {\n",
    "            'x': inst.x, 'y': inst.y, 'z': inst.z,\n",
    "            'rz': inst.rz,\n",
    "            'gripper': inst.gripper,\n",
    "            'name': inst.name,\n",
    "        }\n",
    "\n",
    "\n",
    "class HandEyeResolver:\n",
    "    \"\"\"v1 手眼标定坐标转换 (仅用于对比演示)\n",
    "\n",
    "    对标: HandEyeService.transform() + RobotCalibrateUtil.transfer()\n",
    "    公式: B = k * (R @ A) + T\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self):\n",
    "        self.R = R1\n",
    "        self.T = T1\n",
    "        self.k = k1\n",
    "\n",
    "    def resolve(self, pixel_x: float, pixel_y: float, cam_id: int = 1) -> dict:\n",
    "        A = np.array([[pixel_x], [pixel_y], [0]])\n",
    "        B = self.k * (self.R @ A) + self.T\n",
    "        coords = B.flatten().tolist()\n",
    "        return {'x': round(coords[0], 2), 'y': round(coords[1], 2), 'z': round(coords[2], 2)}\n",
    "\n",
    "\n",
