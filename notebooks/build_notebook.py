    "# ============================================================\n",
    "# Cell 5: TaskRouter\n",
    "# 对标: BaseTaskHandler.TaskController (llmRun + run)\n",
    "# ============================================================\n",
    "\n",
    "@dataclass\n",
    "class TaskInfo:\n",
    "    \"\"\"任务信息 (对标: BaseTaskHandler.TaskInfo)\"\"\"\n",
    "    msg_header: str   # '器件'/'统计'/'清点'/'取消'\n",
    "    id: int           # 器械ID, -1表示无关\n",
    "\n",
    "\n",
    "class TaskRouter:\n",
    "    \"\"\"任务路由调度\n",
    "\n",
    "    对标: TaskController 的 llmRun() 解析 + run() 路由\n",
    "    简化为同步调用（去掉线程循环和时间过期检查）\n",
    "    \"\"\"\n",
    "\n",
    "    def __init__(self, llm: MockLLMService, resolver: PositionResolver):\n",
    "        self._llm = llm\n",
    "        self._resolver = resolver\n",
    "\n",
    "    def process_voice(self, voice_text: str) -> TaskInfo:\n",
    "        \"\"\"语音文本 -> TaskInfo\"\"\"\n",
    "        result = self._llm.process_cmd_text(voice_text)\n",
    "        if isinstance(result, str):\n",
    "            return TaskInfo(msg_header=result, id=-1)\n",
    "        else:\n",
    "            return TaskInfo(msg_header=result[0], id=result[1])\n",
    "\n",
    "    def dispatch(self, task: TaskInfo) -> dict:\n",
    "        \"\"\"任务分发\"\"\"\n",
    "        if task.msg_header == '器件':\n",
    "            if task.id == -1:\n",
    "                return {'status': 'rejected', 'reason': '未识别到器械名称'}\n",
    "            pos = self._resolver.resolve(task.id)\n",
    "            return {'status': 'dispatched', 'handler': 'RobotController', 'position': pos}\n",
    "        elif task.msg_header in ('统计', '清点'):\n",
    "            return {'status': 'dispatched', 'handler': 'CountTaskHandler', 'action': task.msg_header}\n",
    "        elif task.msg_header == '取消':\n",
    "            return {'status': 'dispatched', 'handler': 'CancelMove', 'action': '取消'}\n",
    "        else:\n",
    "            return {'status': 'error', 'reason': f'未知指令: {task.msg_header}'}\n",
    "\n",
    "\n",
    "# --- 验证 ---\n",
    "router = TaskRouter(llm, resolver_v2)\n",
